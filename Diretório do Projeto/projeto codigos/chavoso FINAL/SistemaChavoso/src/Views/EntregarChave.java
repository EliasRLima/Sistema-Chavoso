    private void cbDepItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbDepItemStateChanged
        // TODO add your handling code here:
        cbLab.removeAllItems();
        Departamento dep = (Departamento) cbDep.getSelectedItem();
        int iddep = Chavoso.PegarIdDep(dep);
        for(Laboratorio l: Chavoso.buscaLab(iddep)){
            cbLab.addItem(l.getNumero());
        }
    }//GEN-LAST:event_cbDepItemStateChanged



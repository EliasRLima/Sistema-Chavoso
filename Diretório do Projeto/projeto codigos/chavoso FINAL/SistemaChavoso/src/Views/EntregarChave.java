    private void btn_entregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_entregarMouseClicked
        // TODO add your handling code here:
        if(btnAula.isSelected()){
        Departamento dep = (Departamento) cbDep.getSelectedItem();
        int iddep = Chavoso.PegarIdDep(dep);
        int num = Integer.parseInt(cbLab.getSelectedItem().toString());
        if (!Chavoso.entregarChave(iddep, num)){
            JOptionPane.showMessageDialog(rootPane, "Laboratorio Ocupado ou em Manutenção!", "AVISO", JOptionPane.ERROR_MESSAGE, null);
        }
        this.dispose();
        }
        if(btnLimpeza.isSelected()){
        Departamento dep = (Departamento) cbDep.getSelectedItem();
        int iddep = Chavoso.PegarIdDep(dep);
        int num = Integer.parseInt(cbLab.getSelectedItem().toString());
        if (!Chavoso.entregarChavelimpeza(iddep, num)){
            JOptionPane.showMessageDialog(rootPane, "Laboratorio Ocupado ou em Manutenção!", "AVISO", JOptionPane.ERROR_MESSAGE, null);
        }
        this.dispose();
        }
        if(!btnAula.isSelected() && !btnLimpeza.isSelected()){
            JOptionPane.showMessageDialog(rootPane, "Nenhuma opção selecionada!", "AVISO", JOptionPane.ERROR_MESSAGE, null);
        }
    }//GEN-LAST:event_btn_entregarMouseClicked

